<h3>Lalakhal is situated in Jointapur Upazila about 35 km away from Sylhet city. The location of Lalakhal is just below the Cheraponji of India. This river is originated from Cheraponji hill and flows through the Bangladesh. This is very suitable place to feel the nature very closely. If you go in full moon you never forget the lovely moonlight. You can see the lot of turning in Lalakhal River. Those are beautiful. If you look at the hill, it seems someone decorated those one by one with his own hand. Cloud are frozen on the hills. If you look at closely, the clouds are tied up and stuck on hills together. Sometimes suddenly it would be unsighted within a moment.<br>

