<h2><strong>Sona Mosque</strong></h2>
<h3>Sona Mosque sometimes described as a 'gem of Sultanate architecture' is situated about 3 kilometres' south of kotwali darwaza and half a kilometre to the southeast of the tahkhana complex in the Firuzpur Quarters of gaur-lakhnauti, the capital of Sultanate Bengal. It occupies the western end of the southern bank of a large tank. A little to the west of the mosque is a modern two-storied Guest House, built several years ago by the Department of Archaeology and Museums, Government of Bangladesh. In between the Guest House and the mosque there runs north-south a modern road, which appears to have been of earlier origins, connecting the main city of Gaur-Lakhnauti with its suburb to the south through the Kotwali Darwaza. An inscription tablet still fixed over the central doorway records that the mosque was built by one Majlis-al-Majalis, Majlis Mansur Wali Muhammad bin Ali.<br>

The Arabic letters in the inscription, giving the exact date of construction, have been obliterated. But the name of Sultan Alauddin husain shah (as written in the text...defender of Islam and of the Muslims, Sultan Ala al-dunya wal-din) suggests that the mosque must have been built sometime 
