<h2><strong>About Bandarban</strong></h2>
<h3>Bandarban Hill District is the most remote and least populated district in Bangladesh. The lure of the tallest peaks of Bangladesh, treks through virgin forests and chance to meet more than 15 tribes of the region up close is growing both among Bangladeshis and tourists from other countries. Since the insurgency ceased in the Chittagong Hill Tracts (a cluster that includes all three hill districts of Bangladesh) it has opened up for tourists more than a decade back, though some of the western tourist guides may still describe the area as a major security risk.<br><br>



Map of Bandarban<br><br>
Bandarban, a 4,479 kmÂ² wide area with a population of 292,900 (2003 est.) inside Bangladesh is bordered by Cox's Bazaar, Chittagong, Rangamati and Khagrachari. On the other side of the 129 kilometre international border lies Myanmar provinces of Chin and Arakan.

Bandarban has only one town that approaches anything near a city - the Bandarban town. The rest of the area is divided into 7 upazilas, which are in turn divided into varying numbers of unions. Each union is a cluster of paras and villages.

