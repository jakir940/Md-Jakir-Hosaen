<h2><strong>About Shitakunḍo</strong></h2>
<h3> Shitakunḍo, is an upazila, or administrative unit, in the Chittagong District of Bangladesh. It includes one urban settlement, the Sitakunda Town, and 10 unions, the lowest of administrative units in Bangladesh. It is one of the 14 upazilas, the second tier of administrative units, of the Chittagong District, which also includes 12 thanas, the urban equivalent of upazilas. The district is part of the Chittagong Division, the highest order of administrative units in Bangladesh. Sitakunda is the home of the country's first eco-park, as well as alternative energy projects, specifically wind energy and geothermal power. <br><br>

Sitakunda is one of the oldest sites of human habitation in Bangladesh. During much of its history, it was ruled alternatively by various Buddhist rulers of Myanmar in the east and Muslims rulers of Bengal in the west. For a brief period in the 8th century, it was ruled by the Buddhist Pala Empire of India. The eastern rulers originated from the Kingdom of Arakan, the Mrauk U dynasty, Arakanese pirates and the Pagan Kingdom. The western rulers came from the Sultanate of Bengal and the Mughal province (Suba) of Bangala. European rule of Sitakunda was heralded by Portuguese privateers in 16th and 17th centuries, who ruled together with the pirates; and the British Raj in 18th and 19th centuries, who unified Sitakunda into the rest of the Chittagong District.<br><br>
Economic development in Sitakunda is largely driven by the Dhaka-Chittagong Highway and the railway. Though Sitakunda is predominantly an agricultural area, it also has the largest ship breaking industry in the world. The industry has been accused of neglecting workers' rights, especially concerning work safety practices and child labor. It has also been accused of harming the environment, particularly by causing soil contamination. Sitakunda's ecosystems are further threatened by deforestation, over-fishing, and groundwater contamination. The upazila is also susceptible to natural hazards such as earthquakes, cyclones, and storm surges. It lies on one of the most active seismic faults in Bangladesh, the Sitakunda–Teknaf fault.<br><br>
Sitakunda is renowned for its numerous Islamic, Hindu and Buddhist shrines. It has 280 mosques, 8 mazars, 49 Hindu temples, 4 ashrams, and 3 Buddhist temples. Among its notable religious sites are the Chandranath Temple (a Shakti Peetha or holy pilgrimage site), Vidarshanaram Vihara (founded by the scholar Prajnalok Mahasthavir), and the Hammadyar Mosque (founded by Sultan Ghiyasuddin Mahmud Shah). The attraction of Sitakunda as a tourist destination is elevated by these pilgrimage sites along with the hill range and the eco-park. Despite its diverse population, the area has gone through episodes of communal strife, including attacks on places of worship. There have been reports of activity by the Islamic militant group Jama'atul Mujahideen Bangladesh since the early 2000s.
