<h2><strong>Tanguar haor </strong></h2><br>
<h3>Tanguar haor  located in the Dharmapasha and Tahirpur upazilas of Sunamganj District in Bangladesh, is a unique wetland ecosystem of national importance and has come into international focus. The area of Tanguar haor including 46 villages within the haor is about 100km2 of which 2,802.36 ha2 is wetland. It is the source of livelihood for more than 40,000 people. The Government of Bangladesh declared Tanguar haor as an Ecologically Critical Area in 1999 considering its critical condition as a result of overexploitation of its natural resources. In 2000, the hoar basin was declared a Ramsar site - wetland of international importance. With this declaration, the Government is committed to preserve its natural resources and has taken several steps for protection of this wetland.<br>

Tanguar haor plays an important role in fish production as it functions as a 'mother fishery' for the country.<br>

Every winter the haor is home to about 200 types of migratory birds. The haor is an important source of fish. In 1999-2000, the government earned 7,073,184 takas as revenue just from fisheries of the haor. There are more than 140 species of fresh water fish in the haor. The more predominant among them are: ayir, gang magur, baim, tara, gutum, gulsha, tengra, titna, garia, beti, kakia etc. Hijal, karach, gulli, balua, ban tulsi, nalkhagra and other freshwater wetland trees are in this haor.
